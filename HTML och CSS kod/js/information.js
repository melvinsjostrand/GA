async function getActivities(){

    let path = "https://omsorgapi.azurewebsites.net/Activity/org/2";


    jsonAct = await getactFetch(path);
    jsonAct.activities.forEach(element =>{
        daycalendar(element);
        createSection(element);
    })
}



